def raw_data_to_dfs(input_data_list):
    df_list = []

    for i in range(len(input_data_list)):
        words_list = input_data_list[i]["wordcount"]["words"]
        word_frq_list = input_data_list[i]["wordcount"]["word frequency"]
        df_temp = pd.DataFrame([words_list, word_frq_list], index=['words', 'word_freq']).swapaxes("index",
                                                                                              "columns").sort_values(
            by='word_freq', ascending=False)
        df_list.append(df_temp)

    return df_list


def find_common_words(data_df_list, num_of_highest_freq_common_words=50):
  word_count_df_concat = pd.concat(data_df_list).sort_values(by='word_freq', ascending=False)
  word_count_all_duplicate_df = word_count_df_concat[word_count_df_concat.duplicated('words', keep=False)]
  word_count_all_duplicate_df_ = word_count_all_duplicate_df.groupby('words').agg({'word_freq': 'sum'}).sort_values(
        by='word_freq', ascending=False)
  word_count_all_duplicate_df_ = word_count_all_duplicate_df_.head(num_of_highest_freq_common_words)

  return word_count_all_duplicate_df_


